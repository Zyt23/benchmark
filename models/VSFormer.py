"""QAR-ready VSFormer-style classifier.

The official VSFormer implementation uses additional motif/statistical prior
features generated by preprocessing scripts.  For QAR compact caches we provide
a self-contained value-and-shape transformer adapter: one branch embeds raw
values, another embeds first-order temporal differences (shape), then a
Transformer encoder performs classification.

Source family: https://github.com/Jackxiini/VSFormer
"""

from __future__ import annotations

import torch
import torch.nn as nn


class Model(nn.Module):
    def __init__(self, configs):
        super().__init__()
        self.task_name = configs.task_name
        self.seq_len = int(configs.seq_len)
        self.enc_in = int(configs.enc_in)
        self.num_class = int(getattr(configs, "num_class", 2))
        self.d_model = int(getattr(configs, "d_model", 64))
        self.dropout = float(getattr(configs, "dropout", 0.1))
        self.max_len = min(self.seq_len, int(getattr(configs, "vsformer_max_len", 256)))

        half = max(8, self.d_model // 2)
        self.value_proj = nn.Linear(self.enc_in, half)
        self.shape_proj = nn.Linear(self.enc_in, half)
        self.fuse = nn.Linear(half * 2, self.d_model)
        self.cls_token = nn.Parameter(torch.zeros(1, 1, self.d_model))
        self.pos_embedding = nn.Parameter(torch.randn(1, self.max_len + 1, self.d_model) * 0.02)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model=self.d_model,
            nhead=int(getattr(configs, "n_heads", 8)),
            dim_feedforward=int(getattr(configs, "d_ff", 128)),
            dropout=self.dropout,
            activation=str(getattr(configs, "activation", "gelu")),
            batch_first=True,
        )
        self.encoder = nn.TransformerEncoder(encoder_layer, num_layers=int(getattr(configs, "e_layers", 2)))
        self.norm = nn.LayerNorm(self.d_model)
        self.head = nn.Sequential(
            nn.Dropout(self.dropout),
            nn.Linear(self.d_model, self.num_class),
        )

    @staticmethod
    def _shape_signal(x: torch.Tensor) -> torch.Tensor:
        diff = torch.zeros_like(x)
        diff[:, 1:, :] = x[:, 1:, :] - x[:, :-1, :]
        return diff

    def classification(self, x_enc: torch.Tensor) -> torch.Tensor:
        if x_enc.shape[1] > self.max_len:
            x_enc = torch.nn.functional.adaptive_avg_pool1d(
                x_enc.transpose(1, 2), self.max_len
            ).transpose(1, 2)
        value = self.value_proj(x_enc)
        shape = self.shape_proj(self._shape_signal(x_enc))
        x = self.fuse(torch.cat([value, shape], dim=-1))
        cls = self.cls_token.expand(x.shape[0], -1, -1)
        x = torch.cat([cls, x], dim=1)
        x = x + self.pos_embedding[:, : x.shape[1], :]
        x = self.encoder(x)
        x = self.norm(x[:, 0, :])
        return self.head(x)

    def forward(self, x_enc, x_mark_enc=None, x_dec=None, x_mark_dec=None, mask=None):
        if self.task_name == "classification":
            return self.classification(x_enc)
        raise NotImplementedError("VSFormer adapter currently supports classification only")
